# 反向索引：{自由文本 → 标准类别}
_reverse_index: dict[str, str] | None = None


def _build_reverse_index() -> dict[str, str]:
    """构建反向索引：用户说的词 → 标准类别名"""
    global _reverse_index
    if _reverse_index is not None:
        return _reverse_index

    _reverse_index = {}
    for std_cat, aliases in CATEGORY_SYNONYMS.items():
        if std_cat == "checkin_task_names":
            continue  # 打卡任务名不做类别映射
        for alias in aliases:
            _reverse_index[alias] = std_cat
        # 标准名称也映射自身
        if std_cat not in ("餐饮", "交通", "娱乐", "居家", "学习", "服饰", "礼物",
                           "工资", "奖金", "卖出"):
            continue
        _reverse_index[std_cat] = std_cat
    return _reverse_index


def map_category(raw_text: str) -> tuple[str | None, str]:
    """
    将用户自由文本映射到标准类别。

    返回: (standard_category, match_source)
      - (None, raw) 如果无法映射
      - ("餐饮", "午饭") 如果精确匹配
    """
    index = _build_reverse_index()

    # 1. 精确匹配
    if raw_text in index:
        return index[raw_text], raw_text

    # 2. 子串匹配（"星巴克咖啡" → 先查完整文本，再查关键词 "咖啡"）
    for alias, std_cat in sorted(index.items(), key=lambda x: -len(x[0])):
        if alias in raw_text:
            return std_cat, alias

    return None, raw_text


def map_category_or_other(raw_text: str, default: str = "其他") -> str:
    """映射类别，失败则返回 default"""
    result, _ = map_category(raw_text)
    return result if result else default
