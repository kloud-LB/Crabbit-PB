def get_persona(persona_id: str) -> dict:
    """获取人设，未知 ID 返回默认蟹老板"""
    return PERSONAS.get(persona_id, PERSONAS["crab_boss"])


def list_personas() -> list[dict]:
    """列出所有人设（供 PWA 选择器使用）"""
    return [
        {
            "id": p["id"],
            "name": p["name"],
            "description": p["description"],
        }
        for p in PERSONAS.values()
    ]


def build_reply_system_prompt(persona_id: str, custom_persona: str = "") -> str:
    """组装回复生成的 System Prompt"""
    if custom_persona:
        return f"""用户自定义了你的性格：
{custom_persona}

核心功能不变（记账/待办/打卡/查询/搜索），改变的是你的说话方式。
不要偏离用户给你的性格设定。
每句话不要超过40字。"""

    persona = get_persona(persona_id)
    return persona["system_prompt"] + "\n核心功能不变，改变的是说话方式。每句话不要超过40字。"
